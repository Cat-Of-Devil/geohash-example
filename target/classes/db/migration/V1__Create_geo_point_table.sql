CREATE TABLE GEO_POINT (
  GEO_POINT_ID     SERIAL PRIMARY KEY,
  LATITUDE_DEG     FLOAT8 NOT NULL,
  LONGITUDE_DEG    FLOAT8 NOT NULL,
  GEOHASH          VARCHAR(12) NOT NULL,
  COUNTRY_CODE     VARCHAR(2)
);

CREATE INDEX I_GEO_LOC_LAT_LON ON GEO_POINT (LATITUDE_DEG, LONGITUDE_DEG);
CREATE INDEX I_GEO_LOC_GEOHASH ON GEO_POINT (GEOHASH);
